b = 3.14         # float
c = 2 + 3j       # complex

# Kiểu chuỗi
s = "Hello"      # str

# Kiểu boolean
t = True         # bool

# Kiểu tập hợp
l = [1, 2, 3]    # list
tup = (4, 5)     # tuple
r = range(5)     # range

# Kiểu set
s = {1, 2, 3}    # set
fs = frozenset([1, 2])  # frozenset

# Kiểu dict
d = {"key": "value"}  # dict

# Kiểu None
n = None         # NoneType

